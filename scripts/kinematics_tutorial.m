% 0. 清理之前的缓存和窗口（每次跑新代码的好习惯）
clear; clc; close all;

% 1. 召唤你的重载机械臂 (胡师兄论文参数)
L(1) = Link([0 0.54 0 0], 'modified');
L(2) = Link([0 0 0.2 -pi/2], 'modified');
L(3) = Link([0 0 0.9 0], 'modified');
L(4) = Link([0 0 0.7 0], 'modified');
robot = SerialLink(L, 'name', 'Heavy_Arm');

disp('✅ 机械臂底盘召唤成功！');
disp('----------------------------------------------------');

%% ----------------------------------------------------
% 第一部分：正运动学 (Forward Kinematics, FK)
% 核心概念：给定电机的转动角度 (theta)，求末端夹爪在空间中的位置 (x,y,z)
% ----------------------------------------------------
disp('👉 [第一部分] 正在测试正运动学 (FK)...');

% 设定一组测试的关节角度（4个关节，单位是弧度）
% 比如我们让关节2转动 45度(pi/4)，关节3转动 60度(pi/3)
q_test = [0, pi/4, pi/3, -pi/4];

% 【核心函数 fkine】：正向求解位姿矩阵
T_end = robot.fkine(q_test);

disp('当关节角度输入后，末端到达的空间位姿矩阵 (T) 是：');
disp(T_end); 
% 你会看到一个 4x4 的矩阵，其中最右边那一列的前三个数字，就是对应的 X, Y, Z 坐标！

% 可视化：把这组角度画出来看看
figure(1);
robot.plot(q_test);
title('正运动学演示：已知角度求位置');
disp('请查看 Figure 1 窗口的动画。程序将暂停 3 秒...');
pause(3); % 停顿3秒，方便你观察

disp('----------------------------------------------------');

%% ----------------------------------------------------
% 第二部分：逆运动学 (Inverse Kinematics, IK)
% 核心概念：给定空间中的目标点 (x,y,z)，求电机应该转多少度 (theta)
% 【极其重要】：这是未来衔接大模型视觉识别的唯一桥梁！
% ----------------------------------------------------
disp('👉 [第二部分] 正在测试逆运动学 (IK)...');

% 假设 AI 识别了一张图片，告诉你在 (X=1.2, Y=0.5, Z=0.8) 米处有个水杯
target_X = 1.2;
target_Y = 0.5;
target_Z = 0.8;

% 将 (X,Y,Z) 转化为 MATLAB 认识的齐次变换矩阵 (transl 函数)
Target_T = transl(target_X, target_Y, target_Z);
disp('大模型下达的目标位置是：');
disp(Target_T);

% 【核心函数 ikine & 究极避坑】：求解逆解！
% 避坑指南：胡师兄的机械臂是 4 轴的，但三维空间有 6 个自由度（XYZ+三个姿态角）。
% 如果你直接算，MATLAB 会报错说"无法到达"。
% 所以我们必须加上 'mask', [1 1 1 0 0 0] 这个掩码（Mask）！
% 它的意思是告诉 MATLAB："前三个 1 代表我只要求 X, Y, Z 到达指定位置就行；后三个 0 代表末端的姿态（朝上还是朝下）我不管了，你随便！"
disp('正在疯狂计算各关节需要转动的角度...');
q_inverse = robot.ikine(Target_T, 'mask', [1 1 1 0 0 0]);

disp('计算得出的目标关节角度 (弧度) 为：');
disp(q_inverse);

% 验证一下，让机械臂走到算出来的这个角度，看看有没有到达目标！
figure(2);
robot.plot(q_inverse);
title('逆运动学演示：大模型指令执行完毕！');

disp('🎉 恭喜！正逆运动学跑通，你的机械臂已经具备了接收 AI 指令的初步能力！');